// Latihan switch
let fruit = "";

switch (fruit) {
    case "apple":
        console.log(`${fruit} harganya 2000`);
        break
    case "orange":
        console.log(`${fruit} harganya 3000`);
        break
    case "lemon":
        console.log(`${fruit} hargamya 5000`);
        break
    default:
        console.log(`sorry we're out of ${fruit}`);
}

