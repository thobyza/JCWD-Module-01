// [2] Write a code to check whether the number is odd or even
let num = 7;

if (num % 2 == 1) {
    console.log(`${num} is an odd number.`);
} else {
    console.log(`${num} is an even number.`);
}

// or using ternary
let num2 = 28;
 
console.log(num2 % 2 == 1 ? `${num2} is an odd number.` : `${num2} is an even number.`);


